//可以判断原始类型 除了null
console.log(typeof 2); //number
console.log(typeof true); //boolean
console.log(typeof 'str'); //string
console.log(typeof undefined);
//undefined

console.log(typeof null);
//object

console.log(typeof []);
//object

console.log(typeof function () { });
//function
console.log(typeof {});
//object
